def fizz_buzz(num):
    if (num % 3 == 0 and (num % 5 == 0)):
	    print("fizz buzz")
    elif (num % 3 == 0):
	    print("fizz")
    elif (num % 5 == 0):
	    print("buzz")
    else:
	    print("not fizz or buzz")
fizz_buzz(2)
fizz_buzz(7)
fizz_buzz(96)
